package java.excercise;

public class LuckyInteger {

	public static void main(String[] args) {
		
		 int input[] = {2,2,3,4};
		 int length = input.length;
		 
		 for(int i=0;i<length;i++){
			 
			 if(input[i]==input[i+1]){
				 System.out.println(input[i]);
				 
		 }
		
		 }
		
	}

}
